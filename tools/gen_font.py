# Default glyph for undefined chars (small dot)
DEFAULT = [0x00,0x00,0x18,0x18,0x00,0x00,0x00,0x00]

ATLAS_W, ATLAS_H = 128, 128
FONT_COLS, FONT_ROWS = 16, 16
GLYPH_W, GLYPH_H = 8, 8

pixels = [[0] * ATLAS_W for _ in range(ATLAS_H)]

for code in range(256):
    col = code % FONT_COLS
    row = code // FONT_COLS
    glyph = FONT.get(code, DEFAULT)
    ox, oy = col * GLYPH_W, row * GLYPH_H
    for gy in range(GLYPH_H):
        row_bits = glyph[gy] if gy < len(glyph) else 0
        for gx in range(GLYPH_W):
            bit = (row_bits >> (7 - gx)) & 1
            # ARGB4444: white+opaque if glyph, transparent if not
            pixels[oy+gy][ox+gx] = 0xFFFF if bit else 0x0000

out_dir = os.path.join(os.path.dirname(__file__), '..', 'assets')
os.makedirs(out_dir, exist_ok=True)
out_path = os.path.join(out_dir, 'font.raw')

with open(out_path, 'wb') as f:
    for row in pixels:
        for px in row:
            f.write(struct.pack('<H', px))

print(f"Wrote {ATLAS_W}x{ATLAS_H} ARGB4444 font atlas ({ATLAS_W*ATLAS_H*2} bytes) -> {out_path}")
